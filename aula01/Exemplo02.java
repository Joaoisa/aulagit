public class Exemplo02 {
    public static void main(String[] args) {
        System.out.println("1 + 2"); // aspas só mostra texto
        System.out.println(1 + 2); // fora das arpas não é texto, nesse caso fez a conta de mais.
        System.out.println("1 + 2 = " + 1 + 2);
        System.out.println("1 + 2 = " + (1 + 2) );

        //operadores matemáticos
        // + - * /

        System.out.println( 2 + 3 * 5 );
        System.out.println( (2 + 3) * 5 );

        // ALT + Shift + seta : duplica linha
        //CTRL + F5 : Run
        // syso : atalho para System.out.println
        
        System.out.println( 10 / 2 );
        System.out.println( 11 / 2 );
        System.out.println( 11.0 / 2 ); 

        //constante, nunca muda
        //Variável, arera de memória para guardar informação.
        //Alguns tipos (Basico) de dados : int (Byte, short, int e long) double/boolean/char/String
        // int -  numero interio
        //Double - numeros flutuante
        //String - texto
    }
}
